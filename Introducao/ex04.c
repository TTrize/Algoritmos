#include <stdio.h>

int main(){

  float num, q, c , r;
  
  num > 0;

  printf("Entre com um numero: ");
  scanf("%f", &num);
  
  q = num * num;

  c = num * num * num;

  r = num/2;

  printf("O quadrado do numero: %.2f\n", q);
  printf("O cubo do numero: %.2f\n", c);
  printf("A raiz quadrada do numero: %.2f\n", r);

  return 0;

}
